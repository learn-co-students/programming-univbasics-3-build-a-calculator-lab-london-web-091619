def addition(num1, num2)
result = num1+num2
return result
end

def subtraction(num1, num2)
result1 = num1-num2
return result1
end

def division(num1, num2)
result = num1/num2
return result
end

def multiplication(num1, num2)
result = num1*num2
return result
end

def modulo(num1, num2)
result = num1%num2
return result
end

def square_root(num)
result = Math.sqrt(num)
return result
end
